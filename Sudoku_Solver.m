function value_array = Reduce(index_array,value_array,primes)
%First we sort through all of the columns:
for n=1:9
    for m=1:9
        l = index_array(n,m);
        cell_value = value_array(l);
        if ismember(cell_value, primes)
            %Sort through the row
            for p=1:9
                l2 = index_array(n,p);
                if (~mod(value_array(l2),cell_value) & ~ismember(value_array(l2), primes))
                    value_array(l2) = value_array(l2) / cell_value;
                end
            end
            %Sort through the column
            for q=1:9
                l3 = index_array(q,m);
                if (~mod(value_array(l3),cell_value) & ~ismember(value_array(l3), primes))
                    value_array(l3) = value_array(l3) / cell_value;
                end
            end
            %Sort through the squares. The math is to guarantee the correct
            %square 
            for i=((3*floor((n-0.5)/3))+1):((3*floor((n-0.5)/3))+3)
                for j=((3*floor((m-0.5)/3))+1):((3*floor((m-0.5)/3))+3)
                    l4 = index_array(i,j);
                    if (~mod(value_array(l4),cell_value) & ~ismember(value_array(l4), primes))
                       value_array(l4) = value_array(l4) / cell_value;
                    end
                end
            end
        end
        
    end
end




